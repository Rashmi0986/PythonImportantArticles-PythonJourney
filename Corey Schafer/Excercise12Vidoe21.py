#Sorting Tuples , lists and other objects

li = [0,9,3,4,5,3,6,2,1]
li.sort() # in place sorting 
print(li)

li = [11,33,22,66,77,88,44,99,200,2]
s_li = sorted(li) # returns a new list object which needs to be saved 
print(s_li)

li = [11,33,22,66,77,88,44,99,200,2]
s_li = sorted(li, reverse=True) # returns a new list object which needs to be saved 
print(s_li)

